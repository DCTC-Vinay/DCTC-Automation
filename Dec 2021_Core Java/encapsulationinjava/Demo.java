package encapsulationinjava;

public class Demo {
	
	private float m;
	private float n;
	private float o;
	
	public float getM() {
		return m;
	}
	public void setM(float m) {
		this.m = m;
	}
	public float getN() {
		return n;
	}
	public void setN(float n) {
		this.n = n;
	}
	public float getO() {
		return o;
	}
	public void setO(float o) {
		this.o = o;
	}
	
	
	

}
