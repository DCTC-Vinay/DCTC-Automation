package superkeywordinjava;

public class A {
	
	public A()
	{
		System.out.println("Inside A Class Constructor");
	}
	
	public A(int a)
	{
		System.out.println("Value of a :"+a);
	}
	
	public void show()
	{
		System.out.println("Inside show method of A Class");
	}
	
	public void test()
	{
		System.out.println("In test method of A Class");
	}

}
