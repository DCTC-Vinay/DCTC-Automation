package encapsulationinjava;

public class A {
	
	private int a;
	private float b;
	private char n;
	
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public float getB() {
		return b;
	}
	public void setB(float b) {
		this.b = b;
	}
	public char getN() {
		return n;
	}
	public void setN(char n) {
		this.n = n;
	}
	
	
	
	

}
