package encapsulation;

public class TestEncapsulation {

	private int a=45;
	private float x=78.4f;
	private boolean y=false;
	private char z='q';
	
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public boolean isY() {
		return y;
	}
	public void setY(boolean y) {
		this.y = y;
	}
	public char getZ() {
		return z;
	}
	public void setZ(char z) {
		this.z = z;
	}
	
	
}